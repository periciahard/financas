-- Finanças Família - Supabase seguro por chave familiar via RPC
-- Não cria políticas abertas. O acesso direto às tabelas permanece bloqueado por RLS.

create table if not exists family_states (
  family_key_hash text primary key,
  payload jsonb not null default '{}'::jsonb,
  updated_at timestamptz not null default now()
);

alter table family_states enable row level security;

-- Não criar políticas SELECT/INSERT/UPDATE abertas para anon.
-- O frontend usa somente as funções abaixo.

create extension if not exists pgcrypto;

create or replace function family_hash_key(p_family_key text)
returns text
language sql
stable
as $$
  select encode(digest(coalesce(p_family_key,'') || ':financas-familia-v32-2', 'sha256'), 'hex');
$$;

create or replace function family_save_state(p_family_key text, p_payload jsonb)
returns boolean
language plpgsql
security definer
set search_path = public
as $$
begin
  if p_family_key is null or length(trim(p_family_key)) < 8 then
    raise exception 'Código familiar inválido';
  end if;

  insert into family_states (family_key_hash, payload, updated_at)
  values (family_hash_key(p_family_key), p_payload, now())
  on conflict (family_key_hash)
  do update set payload = excluded.payload, updated_at = now();

  return true;
end;
$$;

create or replace function family_load_state(p_family_key text)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  result jsonb;
begin
  if p_family_key is null or length(trim(p_family_key)) < 8 then
    raise exception 'Código familiar inválido';
  end if;

  select payload into result
  from family_states
  where family_key_hash = family_hash_key(p_family_key);

  return coalesce(result, '{}'::jsonb);
end;
$$;

grant execute on function family_save_state(text, jsonb) to anon, authenticated;
grant execute on function family_load_state(text) to anon, authenticated;


